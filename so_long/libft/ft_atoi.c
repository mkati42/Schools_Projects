#include    "libft.h"

int	ft_atoi(const char *str)
{
	int	z;
	int	j;

	j = 0;
	z = 1;
	while (*str == ' ' || *str == '\t' || *str == '\n')
		str++;
	if (*str == 43 || *str == 45)
	{
		if (*str == 45)
			z *= -1;
		str++;
	}
	while (*str >= 48 && *str <= 57)
	{
		j *= 10;
		j += (*str - '0');
		str++;
	}
	if (z == -1)
		j = -j;
	return (j);
	if (z == 1)
		return (1);
}
